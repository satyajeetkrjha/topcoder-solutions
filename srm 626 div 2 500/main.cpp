class FixedDiceGameDiv2 {
public:
	double getExpectation(int, int);
};

double FixedDiceGameDiv2::getExpectation(int a, int b) {
    double sum=0;
	for(int i=1;i<=a;i++){
        for(int j=1;j<=b;j++){
            if(i>j){
                sum+=1.0/(a*b);
            }
        }
	}
	double expprob=0;
	for(int x=1;x<=a;x++){
        for(int y=1;y<=b;y++){
            if(x>y){
                expprob+=x/(a*b*sum);
            }
        }
	}
	return expprob;


}
