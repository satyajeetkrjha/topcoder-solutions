class NewArenaPassword {
public:
	int minChange(string, int);
};

int NewArenaPassword::minChange(string oldPassword, int K) {
	int n=oldPassword.size();// len of string
	int k=K;
	int ans=0;
	for (int i=0;i<min(n-k,k);i++){
        int co=0;// connected positions
        int freq[26];// count the frequency
        memset(freq,0,sizeof(freq));
        for(int j=i;j<n;j+=n-k)// ,ove n-k ahead from each such j
        {
            freq[oldPassword[j]-'a']++;
            co++;

        }
        int maxii=0;
        for(int p=0;p<26;p++){
            maxii=max(maxii,freq[p]);
        }
        ans+=co-maxii;
	}

	return ans;
}
